def say_hi(name: str, age: int) -> str:
    return "Hi. My name is {} and I'm {} years old".format(name,age)

# Example)
# say_hi("Alex", 32) == "Hi. My name is Alex and I'm 32 years old"
# say_hi("Frank", 68) == "Hi. My name is Frank and I'm 68 years old"
