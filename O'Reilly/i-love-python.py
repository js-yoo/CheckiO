def i_love_python(degree:int):
    if degree==0: return "I hate Python !!!!!!"
    if 1<degree<=4: return "Other languages are better than Python"
    if degree==5: return "Python? It's useful"
    if 6<=degree<=9: return "I'm so happy thanks to Python"
    if degree==10: return "I'm Python, Python is me !!!!"
